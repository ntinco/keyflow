def _entry_to_row(i, e):
    return (
        i,
        e["id"], e["file"], e["type"],
        e.get("key"), e.get("trigger"), e.get("options"),
        e.get("context"), e.get("context_fn"), e.get("context_label"),
        e.get("track_fn"),
        json.dumps(e["track_args"], ensure_ascii=False) if e.get("track_args") is not None else None,
        e["action"], e["label"],
        json.dumps(e["platform"], ensure_ascii=False),
        1 if e.get("active", True) else 0,
        e.get("notes", ""),
    )

def _row_to_entry(row):
    cols = ["sort_order","id","file","type","key","trigger","options","context","context_fn",
            "context_label","track_fn","track_args","action","label","platform","active","notes"]
    d = dict(zip(cols, row))
    d["track_args"] = json.loads(d["track_args"]) if d["track_args"] else None
    d["platform"]   = json.loads(d["platform"])
    d["active"]     = bool(d["active"])
    del d["sort_order"]
    return d

def init_db(source=None):
    """Create/seed hotkeys.db from CATALOG (or supplied list)."""
    entries = source or CATALOG
    con = sqlite3.connect(DB_PATH)
    con.executescript(DB_SCHEMA)
    con.execute("DELETE FROM hotkeys")
    con.executemany(
        "INSERT INTO hotkeys VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)",
        [_entry_to_row(i, e) for i, e in enumerate(entries)]
    )
    con.commit()
    con.close()
    print(f"Seeded {len(entries)} rows → {DB_PATH}")
    print(f"Open in DB Browser for SQLite: https://sqlitebrowser.org/dl/")

def load_db():
    """Load all rows from hotkeys.db ordered by sort_order."""
    con = sqlite3.connect(DB_PATH)
    rows = con.execute("SELECT * FROM hotkeys ORDER BY sort_order").fetchall()
    con.close()
    return [_row_to_entry(r) for r in rows]

def list_hotkeys(entries, inactive_only=False):
    rows = [e for e in entries if (not e["active"]) == inactive_only]
    print(f"{'ID':<30} {'FILE':<22} {'KEY/TRIGGER':<14} {'PLATFORM':<18} {'LABEL'}")
    print("-" * 110)
    for e in rows:
        key = e.get("key") or e.get("trigger") or ""
        plat = ",".join(e.get("platform") or [])
        print(f"{e['id']:<30} {e['file']:<22} {key:<14} {plat:<18} {e['label']}")
    print(f"\n{len(rows)} entries")

def _best_source(label=""):
    """Return entries from hotkeys.db, falling back to in-memory CATALOG."""
    if DB_PATH.exists():
        entries = load_db()
        src = f"hotkeys.db ({len(entries)} rows)"
    else:
        entries = CATALOG
        src = f"in-memory CATALOG ({len(entries)} entries) — run --init-db to create hotkeys.db"
    if label:
        print(f"Source: {src}")
    return entries

