# ---------------------------------------------------------------------------
# CATALOG — one dict per hotkey/hotstring.  This is the single source of truth.
# Edit here to add/remove/disable entries, then run --init-catalog and --sync.
#
# Fields:
#   id            — unique snake_case identifier
#   file          — target .ahk file (relative to hotkeys/, without .ahk)
#   type          — "hotkey" | "hotstring"
#   key           — AHK key combo  (hotkeys only)
#   trigger       — trigger text   (hotstrings only)
#   options       — prefix e.g. :*: or ::  (hotstrings only)
#   context       — winactive() argument; null = global (no #HotIf)
#   context_fn    — raw #HotIf expression when not winactive (rare)
#   context_label — human label used as tracking argument
#   track_fn      — tracking function name; null for hotstrings
#   track_args    — list of args for track_fn
#   action        — AHK statement(s) in the hotkey body
#   label         — human description
#   platform      — list: "windows" and/or "macos"
#   active        — False = skip when generating .ahk (easy disable)
#   notes         — optional free text
# ---------------------------------------------------------------------------

W  = ["windows"]
WM = ["windows", "macos"]

def _hk(id_, file_, key, context, context_fn, context_label, track_fn, track_args, action, label, platform=None, active=True, notes=""):
    return {"id": id_, "file": file_, "type": "hotkey", "key": key,
            "trigger": None, "options": None,
            "context": context, "context_fn": context_fn, "context_label": context_label,
            "track_fn": track_fn, "track_args": track_args,
            "action": action, "label": label,
            "platform": platform or W, "active": active, "notes": notes}

def _hs(id_, file_, trigger, options, action, label, platform=None, active=True, notes=""):
    return {"id": id_, "file": file_, "type": "hotstring",
            "key": None, "trigger": trigger, "options": options,
            "context": None, "context_fn": None, "context_label": None,
            "track_fn": None, "track_args": None,
            "action": action, "label": label,
            "platform": platform or W, "active": active, "notes": notes}

def gk(id_, key, action, label, platform=None, active=True, notes=""):
    """Global hotkey shorthand."""
    return _hk(id_, "global", key, None, None, "global",
               "trackHotkeyEvent", [key, "hotkeys/global.ahk", "global"],
               action, label, platform, active, notes)

def ck(id_, file_, key, context, ctx_label, track_fn, action, label, platform=None, active=True, notes="", context_fn=None):
    """Context hotkey shorthand."""
    return _hk(id_, file_, key, context, context_fn, ctx_label,
               track_fn, [key, ctx_label],
               action, label, platform, active, notes)

def ck1(id_, file_, key, context, ctx_label, track_fn, action, label, platform=None, active=True, notes=""):
    """Context hotkey with single-arg tracking (sap-eclipse style)."""
    return _hk(id_, file_, key, context, None, ctx_label,
               track_fn, [key],
               action, label, platform, active, notes)

