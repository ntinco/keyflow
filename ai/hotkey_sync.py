"""
hotkey_sync.py — keyflow hotkey catalog manager

Usage:
  python ai/hotkey_sync.py --init-db        Create/seed hotkeys.db from built-in CATALOG  ← start here
  python ai/hotkey_sync.py --sync           Read hotkeys.db and regenerate all .ahk hotkey files
  python ai/hotkey_sync.py --list           Print all active hotkeys as a table
  python ai/hotkey_sync.py --list-inactive  Print disabled hotkeys
  python ai/hotkey_sync.py --export-json    Export hotkeys.db to hotkeys-catalog.json (read-only snapshot)

Recommended workflow:
  1. python ai/hotkey_sync.py --init-db         (first time only — seeds hotkeys.db from CATALOG)
  2. Open platforms/windows/data/hotkeys.db in DB Browser for SQLite
  3. Delete rows, set active=0, or add rows — just like Excel
  4. python ai/hotkey_sync.py --sync            (regenerates all .ahk files)

Source of truth: hotkeys.db  (edit this)
Generated artifacts: all .ahk files under platforms/windows/hotkeys/
Optional export: hotkeys-catalog.json  (generated by --export-json, not tracked as source)
"""

