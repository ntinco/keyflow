def _fmt_track(entry):
    args = entry["track_args"]
    quoted = ", ".join(f'"{a}"' for a in args)
    return f'{entry["track_fn"]}({quoted})'

def _fmt_hotif(entry):
    if entry.get("context_fn"):
        return f'#hotif {entry["context_fn"]}'
    if entry["context"]:
        return f'#hotif winactive({entry["context"]})'
    return None

def _render_block(entry):
    lines = []
    if entry["type"] == "hotstring":
        lines.append(f'{entry["options"]}{entry["trigger"]}::{{')
        lines.append(entry["action"])
        lines.append("}")
    else:
        lines.append(f'{entry["key"]}::{{')
        if entry["track_fn"]:
            lines.append(_fmt_track(entry))
        lines.append(entry["action"])
        lines.append("}")
    return "\n".join(lines)

def generate_file(file_key, entries):
    """Generate AHK content for one hotkey file."""
    active = [e for e in entries if e["active"]]
    if not active:
        return ""

    lines = ["; AUTO-GENERATED by ai/hotkey_sync.py"]
    lines.append("; Edit platforms/windows/data/hotkeys-catalog.json, then run: python ai/hotkey_sync.py --sync")
    if file_key in FILE_HEADERS:
        lines.append(FILE_HEADERS[file_key])
    lines.append("")

    # Group entries preserving catalog order, by (context_fn or context or "__global__")
    seen_contexts = []
    groups = {}
    for e in active:
        key = e.get("context_fn") or e.get("context") or "__global__"
        if key not in groups:
            groups[key] = []
            seen_contexts.append(key)
        groups[key].append(e)

    prev_context = None
    for ctx_key in seen_contexts:
        ctx_entries = groups[ctx_key]
        sample = ctx_entries[0]
        new_hotif = _fmt_hotif(sample)

        if new_hotif != prev_context:
            if new_hotif:
                lines.append(new_hotif)
            elif prev_context is not None:
                lines.append("#hotif")
            prev_context = new_hotif

        for e in ctx_entries:
            lines.append(_render_block(e))

    # Close final context
    if prev_context is not None:
        lines.append("#hotif")

    return "\n".join(lines) + "\n"

def sync(catalog_entries):
    """Regenerate all .ahk hotkey files from catalog."""
    by_file = {}
    for e in catalog_entries:
        f = e["file"]
        by_file.setdefault(f, []).append(e)

    written = []
    for file_key, entries in by_file.items():
        path = HOTKEYS_DIR / (file_key + ".ahk")
        path.parent.mkdir(parents=True, exist_ok=True)
        content = generate_file(file_key, entries)
        path.write_text(content, encoding="utf-8")
        written.append(str(path))
    return written

def export_json():
    """Export current hotkeys.db to hotkeys-catalog.json (read-only snapshot for AI/review)."""
    entries = load_db() if DB_PATH.exists() else CATALOG
    doc = {
        "description": "Read-only export from hotkeys.db. Do NOT edit this file — edit hotkeys.db instead, then run: python ai/hotkey_sync.py --sync",
        "generated_by": "python ai/hotkey_sync.py --export-json",
        "version": "1.0.0",
        "hotkeys": entries
    }
    CATALOG_PATH.write_text(json.dumps(doc, indent=2, ensure_ascii=False), encoding="utf-8")
    print(f"Exported {len(entries)} entries → {CATALOG_PATH}")

